export const generateWeakJP = (arr: any) => {
  const result = [];
  for (let i = 0; i < arr.length; i += 1) {
    const value = arr[i];
    const match = weakVNs.find((item) => item.value === value);
    result.push(match ? weakJPs[match.index].value : value);
  }
  return result;
};
