export const generateStrongJP = (arr: any) => {
  const result = [];
  for (let i = 0; i < arr.length; i += 1) {
    const value = arr[i];
    const match = strongVNs.find((item) => item.value === value);
    result.push(match ? strongJPs[match.index].value : value);
  }
  return result;
};
